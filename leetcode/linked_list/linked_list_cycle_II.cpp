struct ListNode {
    int val;
    ListNode *next;
    ListNode(int x) : val(x), next(NULL) {}
};

ListNode *detectCycle(ListNode *head) {
    set<ListNode*> st;
    ListNode* node = head;
    int i = 0;
    while(node != NULL){
        if(st.find(node) != st.end()){
            return *(st.find(node));
        }
        st.insert(node);
        node = node->next;
        i++;
    }
    return NULL;
}

